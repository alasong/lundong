def add_constituents():
    """
    添加成分股到数据库
    """
    db = get_database()

    total_added = 0

    for concept_code, concept_name, stock_indices in CONCEPT_DEFINITIONS:
        # 获取股票列表
        stocks = [CORE_STOCKS[i] for i in stock_indices if i < len(CORE_STOCKS)]

        if not stocks:
            logger.warning(f"板块 {concept_code} 无股票")
            continue

        # 转换为数据库格式
        constituents = [{'stock_code': s[0], 'stock_name': s[1]} for s in stocks]

        # 保存到数据库
        db.save_concept_constituents(concept_code, constituents)
        total_added += len(constituents)

        logger.info(f"保存 {concept_code} ({concept_name}): {len(constituents)} 只成分股")

    logger.info(f"\n完成：共导入 {total_added} 条成分股记录")

    # 显示统计
    conn = sqlite3.connect('data/stock.db')
    cursor = conn.cursor()

    cursor.execute("""
        SELECT concept_code, COUNT(*) as stock_count
        FROM concept_constituent
        GROUP BY concept_code
        ORDER BY stock_count DESC
        LIMIT 30
    """)

    print("\n" + "=" * 70)
    print("成分股统计（TOP 30）")
    print("=" * 70)
    for row in cursor.fetchall():
        print(f"  {row[0]}: {row[1]} 只")

    cursor.execute("SELECT COUNT(DISTINCT concept_code) FROM concept_constituent")
    concept_count = cursor.fetchone()[0]

    cursor.execute("SELECT COUNT(DISTINCT stock_code) FROM concept_constituent")
    unique_stocks = cursor.fetchone()[0]

    print("=" * 70)
    print(f"总计：{concept_count} 个板块，{unique_stocks} 只不同股票")
    print("=" * 70)

    conn.close()

    return total_added


